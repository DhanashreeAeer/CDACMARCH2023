class LabstarPattern7{
	public static void main(String args[])
	{
		for(int i=1;i<=5;i++)
		{
			for (int s=5-i;s>=1;s--){
			System.out.print(" ");}
			for(int j=1;j<=(2*i)-1;j++)
			{
					
				System.out.print("*");
			}
				
		System.out.println(" ");
	}
		for(int i=4;i>=1;i--)
		{
			for (int s=4;s>=i;s--){
			System.out.print(" ");}
			for(int j=1;j<=(2*i)-1;j++)
			{
					
				System.out.print("*");
			}
				
		System.out.println(" ");
	}
	}
}